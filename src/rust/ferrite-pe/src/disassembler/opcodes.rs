/// Format an operand for display in IL disassembly.
pub fn format_operand(operand: &Operand, resolver: &MetadataResolver) -> String {
    match operand {
        Operand::None => String::new(),
        Operand::Int8(v) => format!("{}", v),
        Operand::Int32(v) => format!("{}", v),
        Operand::Int64(v) => format!("{}", v),
        Operand::Float32(v) => format!("{}", v),
        Operand::Float64(v) => format!("{}", v),
        Operand::Token(tok) => resolver.resolve_token(*tok),
        Operand::StringToken(tok) => resolver.resolve_token(*tok),
        Operand::BranchTarget(offset) => format!("IL_{:04x}", offset),
        Operand::Switch(targets) => {
            let labels: Vec<String> = targets.iter().map(|t| format!("IL_{:04x}", t)).collect();
            format!("({})", labels.join(", "))
        }
        Operand::Var(idx) => format!("V_{}", idx),
        Operand::ResolvedName(name) => name.clone(),
    }
}
