/// A decoded CIL instruction.
#[derive(Debug, Clone, PartialEq)]
pub struct Instruction {
    /// Instruction index (used as offset when parsing via dotnetdll).
    pub offset: u32,
    pub opcode: OpCode,
    pub operand: Operand,
}

/// Operand carried by an instruction.
#[derive(Debug, Clone, PartialEq)]
pub enum Operand {
    None,
    Int8(i8),
    Int32(i32),
    Int64(i64),
    Float32(f32),
    Float64(f64),
    Token(u32),
    StringToken(u32),
    BranchTarget(i64),
    Switch(Vec<i64>),
    Var(u16),
    /// Pre-resolved name (for type operations where we already know the name).
    ResolvedName(String),
}

